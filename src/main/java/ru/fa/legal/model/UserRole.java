package ru.fa.legal.model;

/**
 * Перечисление ролей пользователей в системе.
 * Определяет уровень доступа и функциональные возможности.
 */
public enum UserRole {
    /**
     * Клиент - обычный пользователь системы.
     * Может просматривать свои дела и консультации, записываться на консультации.
     */
    CLIENT,

    /**
     * Юрист - сотрудник фирмы.
     * Может работать с делами, проводить консультации, просматривать документы.
     */
    LAWYER,

    /**
     * Менеджер - управляющий персонал.
     * Имеет доступ к статистике, управлению пользователями и делами.
     */
    MANAGER,

    /**
     * Администратор - полный доступ к системе.
     * Может управлять пользователями, их ролями, всеми данными системы.
     */
    ADMIN
}
